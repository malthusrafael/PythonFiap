#4. Calcular e exibir a média aritmética de quatro valores quaisquer que serão digitados.
print('Digite 4 valores para calcular a média')
a1 = float(input('Digite o primeiro valor: '))
a2 = float(input('Digite o segundo valor: '))
a3 = float(input('Digite o terceiro valor: '))
a4 = float(input('Digite o quarto valor :'))
m = (a1+a2+a3+a4)/4
print(f'A media dos quatro valores é {m}')
